#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *src)
{
	char		*str;
	int			len;
	int			i;

	len = ft_strlen(src);
	i = 0;
	str = malloc(len + 1);
	if (!str)
		return (NULL);
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
